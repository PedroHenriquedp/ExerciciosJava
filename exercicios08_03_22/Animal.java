package exercicios08_03_22;

public class Animal {

	//Attributes
	private	String nome;
	private int idade;
	private String som;
	
	//Method Setters/Getters
	
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public int getIdade() {
		return idade;
	}
	public void setIdade(int idade) {
		this.idade = idade;
	}
	public String getSom() {
		return som;
	}
	public void setSom(String som) {
		this.som = som;
	}

}
